enum Layers{
    L_BASE, L_LOWER, L_RAISE, L_ADJUST
};

void oled_render_layer_state_r2g(void) {
    oled_write_ln_P(PSTR("LAYER"), false);
    switch (get_highest_layer(layer_state)) {
        case L_BASE:
            oled_write_ln_P(PSTR("ZERO\n"), false);
            break;
        case L_LOWER:
            oled_write_ln_P(PSTR("ONE\n"), false);
            break;
        case L_RAISE:
            oled_write_ln_P(PSTR("TWO\n"), false);
            break;
        case L_ADJUST:
            oled_write_ln_P(PSTR("ADJ\n"), false);
            break;
    }
}

